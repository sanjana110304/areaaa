public class SumOfThreeNumbers {
    public static void main(String[] args) {
        // Define the three numbers
        double num1 = 10.5;
        double num2 = 20.7;
        double num3 = 30.3;

        // Calculate the sum
        double sum = num1 + num2 + num3;

        // Display the result
        System.out.println("The sum of the three numbers is: " + sum);
    }
}
